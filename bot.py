@bot.message_handler(func=lambda message: True)
def handle_message(message):
    user_message = message.text
    doc = nlp(user_message)

    entities = {label: [] for label in ENTITIES_TO_FIND}

    for ent in doc.ents:
        if ent.label_ in entities:
            if ent.text not in entities[ent.label_]:
                entities[ent.label_].append(ent.text)

    response_parts = [
        f"{LABELS_RU.get(label, label)}: {', '.join(values)}"
        for label, values in entities.items() if values
    ]

    if response_parts:
        response = "\n".join(response_parts)
        closing_msg = random.choice(CLOSING_PHRASES)
        advice_msg = random.choice(ADVICE_MESSAGES)
        response += closing_msg + advice_msg
    else:
        response = random.choice(NO_ENTITIES_MESSAGES)

    bot.reply_to(message, response)

